package com.Switch_case;

public class switch_case {
    public static void main(String[] args) {
        int day = 3;
        switch (day){
            case 1:
                System.out.println("This is 1");
                break;
            case 2:
                System.out.println("This is 2");
                break;
            case 3:
                System.out.println("This is 3");
                break;
        }
    }
}
